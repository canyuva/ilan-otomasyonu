
public class Renk {
    private String renk;
    private int ID;

    public Renk(String renk, int ID) {
        this.renk = renk;
        this.ID = ID;
    }

    public Renk(String renk) {
        this.renk = renk;
    }

    public Renk() {
    }

    public String getRenk() {
        return renk;
    }

    public void setRenk(String renk) {
        this.renk = renk;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    
    
}
