    /* ------------------------------------------------
    
        goster Methodlarının işlevleri yine hepsi birbirine benziyor ve şu şekilde :
        
        1- Türe göre türün nesnesine atılıyor :)
            Örnek : gosterIlan() methodunda ilan nesnesi kullanılıyor.
            Tabi bu nesneler yukardaki global değişkenler ve nesneler bölümünde görüldüğü üzere
            ArrayList olarak tutuluyor böyle combobox'a eklerken kolaylık sağlıyor.
    
        1- Veritabani nesnesini kullanarak veritabanindaki göster methodu (SQL sorguları içeriyor) 
        çağrılıp gerekli nesneye atanıyor.
    
        2- DefaultComboBoxModel ile temsili (veya geçici) String tipinde combobox modelinin nesnesini oluşturuyoruz.
    
        3- For döngüsüyle cm nesnesine gözükecek değeri ekliyoruz. 
            Örneğin Renk combobox'ında sarı,kırmızı,siyah,beyaz renkleri bulunuyorsa 
            for'un içinde oluşturduğumuz r nesnesinin (örneğin) getter setter methoduyla ismini çekiyoruz.  
            Böylece renk isimleri combobox'da gözüküyor.
    
        4- setModel methoduyla işlem yaptığımız temsili combobox modelini 
            Design kısmında bulunan combobox'a atayıp orda kullanmaya başlıyoruz.
    
        5- Son olarak formu ilk açtığımızda combobox'ların ilk elemanları seçili olmuyor.
        secili-- değişkenine get(0) ile atamasını yapmamız gerekiyor.
        
        
    
    --------------------------------------------------*/
    
    void gosterIlan() throws SQLException{
        
        // 1
        ilan = vt.gosterIlan();
        // 2
        DefaultComboBoxModel<String> cm=new DefaultComboBoxModel<>();
        // 3
        for(Ilan i:ilan){
            cm.addElement(i.getIlanAdi());
            System.out.println(i.getIlanAdi());
        }
        
        
        
        // 4 - Eklenen verileri combobox modeline atama işlemi yapılır.
        ilansilCB.setModel(cm);
        ilan_upCB.setModel(cm);
        
        // 5
        seciliIlan = ilan.get(0).getID();
    }
    
    void gosterAraba() throws SQLException{
        araba = vt.gosterAraba();
        DefaultComboBoxModel<String> cm=new DefaultComboBoxModel<>();
        for(Araba a:araba){
            System.out.println(a.getMarka());
            cm.addElement(""+a.getMarka()+" "+a.getModel()+" "+a.getRenkID()+ " "+a.getVitesID() + " " + a.getYakitID() );
        }
        
        // Eklenen verileri combobox modeline atama işlemi yapılır.
        arabaCB.setModel(cm);
        arabaCB1.setModel(cm);
        arabasilCB.setModel(cm);
        araba_upCB.setModel(cm);
        
        seciliAraba = araba.get(0).getID();
    }
    
