
public class Vites {
    private String vites;
    private int ID;

    public Vites(String vites, int ID) {
        this.vites = vites;
        this.ID = ID;
    }

    public Vites(String vites) {
        this.vites = vites;
    }

    public Vites() {
    }



    public String getVites() {
        return vites;
    }

    public void setVites(String vites) {
        this.vites = vites;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }
    
    
    
}
